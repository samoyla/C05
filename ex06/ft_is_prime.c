int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb > 1)
	{
		while (2 * i < nb)
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}

#include <stdio.h>

int	main()
{
	printf("%d\n", ft_is_prime(97));
}
